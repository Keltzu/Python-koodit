numero1 = int(input("anna eka luku: "))
numero2 = int(input("anna toka luku: "))
numero3 = int(input("anna kolmas luku: "))

summa = numero1 + numero2 + numero3
tulo = numero1 * numero2 * numero3
keskiarvo = summa / 3

